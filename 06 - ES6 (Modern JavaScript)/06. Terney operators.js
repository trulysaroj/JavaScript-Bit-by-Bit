/* The ternary operator, also known as the conditional operator, 
allows you to write shorter and more concise conditional statements.  */ 


// Syntax
// condition ? expression1 : expression2


///////////////////////////////////////////////////////////////////////////////////////////////


// example: 1

const age = 20;
const isAdult = age >= 18 ? "Yes" : "No";

console.log(isAdult); // Output: "Yes"


///////////////////////////////////////////////////////////////////////////////////////////////



// example: 2

let price = 1000;
const allowDiscount = price > 700 ? 'Allow' : 'Not Allow';

console.log(allowDiscount);  // Output: Allow



