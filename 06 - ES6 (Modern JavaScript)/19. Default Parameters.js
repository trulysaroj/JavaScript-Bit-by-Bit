/* Default parameters:
   -> to assign default values to function parameters if no argument or an undefined argument is provided */ 


// *Syntax:
// function functionName(parameter = defaultValue) {
    // Function body
//   }
  

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// Example: 1

function checkPassion(passion = 'Coding') {  // Coding is default parameter  value here
    console.log(`My Passion is ${passion}`);
  
};


checkPassion();  // My Passion is Coding  
checkPassion('Learning');  // My Passion is Learning


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// Example: 2
function sum(a, b = 5) {
    return a + b;
};

console.log(sum(1));     // 6  (1 + 5)
console.log(sum(2, 3));  // 5  (2 + 3)



