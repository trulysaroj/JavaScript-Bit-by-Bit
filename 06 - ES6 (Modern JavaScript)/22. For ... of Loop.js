/* For .. of Loop 
 -> easy and concise way to iterate over iterable objects, such as arrays, strings, and other collections.
 -> It simplifies the process of iterating through the elements of an iterable without the need for manual index management.*/ 


// *Syntax:

// for (variable of iterable) {
     // Code block to be executed
//   }
  

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// Example: 1

const scores = [80, 70, 99, 87];

let totalScore = 0;
for(let score of scores) {
    totalScore += score
};

console.log(totalScore);  // 336
 

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// Example: 2 (in String)

const message = "Hello";

for (let char of message) {
  console.log(char);   // H, e, l , l, o
}


