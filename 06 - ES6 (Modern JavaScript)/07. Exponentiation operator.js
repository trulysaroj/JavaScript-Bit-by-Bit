
///////////////////////////////////////////////////////////////////////////////////////////////

/* Exponentiation operator (**): 
It's introduced in ECMAScript 2016 (ES7) as a new feature for performing exponentiation calculations.*/ 


///////////////////////////////////////////////////////////////////////////////////////////////


// example: 1

const base = 3;
const exponent = 3;
const result = base ** exponent;

console.log(result);  // 27



///////////////////////////////////////////////////////////////////////////////////////////////


// example: 2

const expo = 2 ** 3;
console.log(expo);   // 8



///////////////////////////////////////////////////////////////////////////////////////////////



// *Note:-  JavaScript also provides the Math.pow() function for exponentiation calculations

console.log(Math.pow(2, 3));  // 8
console.log(Math.pow(3, 3));  // 27


